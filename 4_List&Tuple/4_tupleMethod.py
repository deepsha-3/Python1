#  tuple methods in Python

a = (1, 2, 3, 2, 4, 2, 5)

# count() method: returns the number of times a value appears in the tuple

no= a.count(2)
print(no)


# index() method: returns the first index of the value

i=a.index(5)
print(i)




# Note: Tuples only have two built-in methods: count() and index()


