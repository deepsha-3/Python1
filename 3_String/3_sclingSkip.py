# String slicing with skips is a way to access characters in a string while skipping certain characters based on a specified step.

# String slicing with skips is done using the colon (:) operator along with a step value.

name = "Dipisha" 
name_slice = name[0:7:3]  # Accessing characters from index 0 to 6 with a step of 2 
print(name_slice)  # Output: Dpa