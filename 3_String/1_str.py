# String is a sequence of characters which closed in single or double quotes.

# String can be created using single quotes, double quotes, or triple quotes.
 
# single quotes
name = 'Dipisha'
print(name)     # Output: Dipsha

# double quotes
name = "Deepsha"
print(name)     # Output: Deepsha

# triple quotes
name = """Deekshya"""
print(name)     # Output: Deekshya

name = '''Kartik'''
print(name)     # Output: Kartik