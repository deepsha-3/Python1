#  Operators in Python

# 1. Arithmetic Operators:
'''These operators are used to perform mathematical operations.'''
   # Here are the arithmetic operators in Python:
a = 2
b = 4
c = a + b
print(c)  # Output: 6


# 2. Assignment Operators
'''Assignment operators are used to assign values to variables. They can also perform operations and assign the result back to the variable.'''
   # Here are the assignment operators in Python:
a = 5
b = 10  
a += b  # Equivalent to a = a + b
print(a)  # Output: 15


# 3. Comparision operators / Relational operators
'''Comparison operators are used to compare two values. They return a boolean value (True or False) based on the comparison.'''   
    # Here are the comparison operators in Python: 
a= 3<2
print(a)  # Output: False

b= 3>2
print(b)  # Output: True


# 4. Logical Operators
'''Logical operators are used to combine conditional statements. They return a boolean value based on the logical operation.'''
   # Here are the logical operators in Python:
a = True
b = False
c = a and b       # Logical AND
print(c)     # Output: False

d = a or b        # Logical OR
print(d)     # Output: True

e = not a         # Logical NOT
print(e)     # Output: False